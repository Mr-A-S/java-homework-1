public class Task6 {
    public static void main(String[] args) {

        // Используем тип double, так как результат будет дробным
        double harmonicSum = 0.0;

        System.out.println("Первые 10 членов гармонического ряда:");

        // Цикл от 1 до 10
        for (int i = 1; i <= 10; i++) {
            // К текущей сумме добавляем следующий член ряда (1 / i)
            // Важно использовать 1.0, чтобы деление было дробным, а не целочисленным
            harmonicSum = harmonicSum + (1.0 / i);

            // System.out.printf() - это специальный метод для форматированного вывода
            // H%d = заменяется на номер шага (i)
            // %.4f = заменяется на сумму (harmonicSum), отформатированную до 4 знаков после запятой
            // \n = перенос на новую строку
            System.out.printf("H%-2d = %.4f\n", i, harmonicSum);
        }
    }
}